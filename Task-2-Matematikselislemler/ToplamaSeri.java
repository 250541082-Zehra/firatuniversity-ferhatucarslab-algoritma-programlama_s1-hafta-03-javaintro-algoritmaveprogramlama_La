public class ToplamaSeri {
    public static void main(String[] args) {

        // Sonucu hesapla
        // Toplama işlemi doğrudan yapılıyor: 1+2+3+4+5+6+7+8+9 = 45
        int sonuc = 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9;

        // Ekrana yazdir
        // "1+2+...+9 = " metni ile 'sonuc' değişkeninin değeri birleştirilip yazdırılıyor.
        System.out.println("1+2+...+9 = " + sonuc);

    }
}
